    result = []
    for i in range(len(numbers)):
        for j in range(i + 1, len(numbers)):
            str_i, str_j = str(numbers[i]), str(numbers[j])
            if len(str_i) == len(str_j) and sum(1 for a, b in zip(str_i, str_j) if a != b) == 1:
                result.append((numbers[i], numbers[j]))
